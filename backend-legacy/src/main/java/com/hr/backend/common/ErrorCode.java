/**
 * ================================================================================
 * 人力资源数据中心系统 - 错误码枚举类
 * ================================================================================
 * 项目名称：HrDataCenter（人力资源数据中心系统）
 * 模块名称：通用组件模块（common）
 *
 * 类说明：ErrorCode
 * 本类定义了系统中所有标准错误码，采用枚举类型实现
 * 用于统一管理系统中的错误码和错误消息，避免魔法值的使用
 *
 * 设计目的：
 * 1. 集中管理所有错误码，便于维护和查找
 * 2. 提供类型安全的错误码定义，避免拼写错误
 * 3. 配合BaseException使用，提供标准的错误信息
 * 4. 使用HTTP状态码风格，符合RESTful规范
 *
 * 错误码设计原则：
 * - 2xx：成功
 * - 4xx：客户端错误（参数错误、未授权、权限不足等）
 * - 5xx：服务器错误（系统错误）
 * ================================================================================
 */
package com.hr.backend.common; // 声明包名，表示这个类属于com.hr.backend.common包

// 导入Lombok的Getter注解，用于自动生成getter方法
import lombok.Getter;

/**
 * 错误码枚举
 * <p>
 * 定义了系统中所有标准错误码和对应的错误消息
 * 使用Lombok的@Getter注解自动生成getter方法
 *
 * 枚举（enum）是Java中一种特殊的类，用于定义一组常量
 * 使用枚举的好处：
 * 1. 类型安全：编译时会检查，避免拼写错误
 * 2. 可读性好：使用有意义的名称代替魔法值
 * 3. 易于维护：集中管理，修改方便
 */
@Getter // 使用Lombok注解，自动生成所有枚举常量的getter方法（getCode()和getMessage()）
public enum ErrorCode { // 定义ErrorCode枚举类，enum关键字表示这是一个枚举类型

    /**
     * 成功
     * <p>
     * 使用场景：操作成功完成
     * 错误码：200 - HTTP标准状态码，表示请求成功
     */
    SUCCESS(200, "成功"), // 定义枚举常量SUCCESS，调用构造方法传入错误码200和错误消息"成功"

    /**
     * 参数错误
     * <p>
     * 使用场景：
     * - 请求参数缺失
     * - 参数格式不正确
     * - 参数校验失败
     * 错误码：400 - HTTP标准状态码，表示客户端请求参数错误
     */
    PARAM_ERROR(400, "参数错误"), // 定义枚举常量PARAM_ERROR，调用构造方法传入错误码400和错误消息"参数错误"

    /**
     * 未授权
     * <p>
     * 使用场景：
     * - 用户未登录
     * - Token无效或过期
     * - 用户名或密码错误
     * 错误码：401 - HTTP标准状态码，表示未认证
     */
    UNAUTHORIZED(401, "未授权"), // 定义枚举常量UNAUTHORIZED，调用构造方法传入错误码401和错误消息"未授权"

    /**
     * 权限不足
     * <p>
     * 使用场景：
     * - 用户已登录但无权访问该资源
     * - 角色权限不足
     * 错误码：403 - HTTP标准状态码，表示禁止访问
     */
    FORBIDDEN(403, "权限不足"), // 定义枚举常量FORBIDDEN，调用构造方法传入错误码403和错误消息"权限不足"

    /**
     * 资源不存在
     * <p>
     * 使用场景：
     * - 查询的用户不存在
     * - 请求的接口不存在
     * - 访问的数据不存在
     * 错误码：404 - HTTP标准状态码，表示资源未找到
     */
    NOT_FOUND(404, "资源不存在"), // 定义枚举常量NOT_FOUND，调用构造方法传入错误码404和错误消息"资源不存在"

    /**
     * 用户已存在
     * <p>
     * 使用场景：
     * - 注册时用户名已存在
     * - 创建重复资源
     * 错误码：409 - HTTP标准状态码，表示冲突
     */
    USER_EXISTS(409, "用户已存在"), // 定义枚举常量USER_EXISTS，调用构造方法传入错误码409和错误消息"用户已存在"

    /**
     * 系统错误
     * <p>
     * 使用场景：
     * - 数据库连接失败
     * - 网络异常
     * - 服务器内部错误
     * 错误码：500 - HTTP标准状态码，表示服务器内部错误
     */
    SYSTEM_ERROR(500, "系统错误"); // 定义枚举常量SYSTEM_ERROR，调用构造方法传入错误码500和错误消息"系统错误"

    /**
     * 错误码
     * <p>
     * 使用HTTP状态码风格，便于理解和使用
     * final关键字表示这个字段一旦赋值就不能修改
     */
    private final int code; // 定义错误码字段，int类型，final表示不可变

    /**
     * 错误消息
     * <p>
     * 描述错误的具体原因，用于展示给用户
     * final关键字表示这个字段一旦赋值就不能修改
     */
    private final String message; // 定义错误消息字段，String类型，final表示不可变

    /**
     * 构造方法
     * <p>
     * 枚举类的构造方法必须是私有的（private），不能在外部调用
     * 当定义枚举常量时（如SUCCESS(200, "成功")），会自动调用此构造方法
     *
     * @param code    错误码，int类型
     * @param message 错误消息，String类型
     */
    ErrorCode(int code, String message) { // 定义枚举类的构造方法，接收错误码和错误消息两个参数
        this.code = code; // 将传入的错误码参数赋值给当前枚举常量的code字段
        this.message = message; // 将传入的错误消息参数赋值给当前枚举常量的message字段
    }
}

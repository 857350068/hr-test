/**
 * ================================================================================
 * 人力资源数据中心系统 - 错误码枚举类
 * ================================================================================
 * 项目名称：HrDataCenter（人力资源数据中心系统）
 * 模块名称：通用组件模块（common）
 *
 * 类说明：ErrorCode
 * 本类定义了系统中所有标准错误码，采用枚举类型实现
 * 用于统一管理系统中的错误码和错误消息，避免魔法值的使用
 *
 * 设计目的：
 * 1. 集中管理所有错误码，便于维护和查找
 * 2. 提供类型安全的错误码定义，避免拼写错误
 * 3. 配合BaseException使用，提供标准的错误信息
 * 4. 使用HTTP状态码风格，符合RESTful规范
 *
 * 错误码设计原则：
 * - 2xx：成功
 * - 4xx：客户端错误（参数错误、未授权、权限不足等）
 * - 5xx：服务器错误（系统错误）
 * ================================================================================
 */
package com.hr.backend.common;

import lombok.Getter;

/**
 * 错误码枚举
 * <p>
 * 定义了系统中所有标准错误码和对应的错误消息
 * 使用Lombok的@Getter注解自动生成getter方法
 */
@Getter
public enum ErrorCode {

    /**
     * 成功
     * <p>
     * 使用场景：操作成功完成
     */
    SUCCESS(200, "成功"),

    /**
     * 参数错误
     * <p>
     * 使用场景：
     * - 请求参数缺失
     * - 参数格式不正确
     * - 参数校验失败
     */
    PARAM_ERROR(400, "参数错误"),

    /**
     * 未授权
     * <p>
     * 使用场景：
     * - 用户未登录
     * - Token无效或过期
     * - 用户名或密码错误
     */
    UNAUTHORIZED(401, "未授权"),

    /**
     * 权限不足
     * <p>
     * 使用场景：
     * - 用户已登录但无权访问该资源
     * - 角色权限不足
     */
    FORBIDDEN(403, "权限不足"),

    /**
     * 资源不存在
     * <p>
     * 使用场景：
     * - 查询的用户不存在
     * - 请求的接口不存在
     * - 访问的数据不存在
     */
    NOT_FOUND(404, "资源不存在"),

    /**
     * 用户已存在
     * <p>
     * 使用场景：
     * - 注册时用户名已存在
     * - 创建重复资源
     */
    USER_EXISTS(409, "用户已存在"),

    /**
     * 系统错误
     * <p>
     * 使用场景：
     * - 数据库连接失败
     * - 网络异常
     * - 服务器内部错误
     */
    SYSTEM_ERROR(500, "系统错误");

    /**
     * 错误码
     * <p>
     * 使用HTTP状态码风格，便于理解和使用
     */
    private final int code;

    /**
     * 错误消息
     * <p>
     * 描述错误的具体原因，用于展示给用户
     */
    private final String message;

    /**
     * 构造方法
     *
     * @param code    错误码
     * @param message 错误消息
     */
    ErrorCode(int code, String message) {
        this.code = code;
        this.message = message;
    }
}

SELECT '员工数据插入完成!' AS message;
SELECT '======================================' AS '';
SELECT
    department AS 部门,
    COUNT(*) AS 人数
FROM employee
WHERE deleted = 0
GROUP BY department
ORDER BY department;

SELECT '======================================' AS '';
SELECT '员工状态统计:' AS message;
SELECT
    CASE status
        WHEN 0 THEN '离职'
        WHEN 1 THEN '在职'
        WHEN 2 THEN '试用'
    END AS 状态,
    COUNT(*) AS 人数
FROM employee
WHERE deleted = 0
GROUP BY status
ORDER BY status;

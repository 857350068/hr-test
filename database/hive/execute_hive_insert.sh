#!/bin/bash
# =====================================================
# Hive数据插入执行脚本
# 项目: 人力资源数据中心
# 用途: 执行Hive数据插入SQL脚本
# 使用方法: ./execute_hive_insert.sh
# =====================================================

echo "======================================"
echo "开始执行Hive数据插入脚本"
echo "======================================"

# 检查Hive是否可用
if ! command -v hive &> /dev/null; then
    echo "错误: Hive命令未找到，请确保Hive已正确安装并配置"
    exit 1
fi

# 执行Hive脚本
hive -f /opt/database/hive/hive_data_insert_final.sql

# 检查执行结果
if [ $? -eq 0 ]; then
    echo "======================================"
    echo "Hive数据插入执行成功!"
    echo "======================================"
else
    echo "======================================"
    echo "Hive数据插入执行失败!"
    echo "请检查错误信息并重试"
    echo "======================================"
    exit 1
fi

exit 0

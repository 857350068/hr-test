-- ------------------------------------------------------------
-- 4.1 组织效能分析视图（对应 /api/analysis/organization-efficiency）
-- ------------------------------------------------------------
DROP VIEW IF EXISTS hr_db.vw_organization_efficiency;
CREATE VIEW hr_db.vw_organization_efficiency AS
SELECT
  dept_id,
  dept_name,
  AVG(value) AS avg_score,
  COUNT(DISTINCT employee_no) AS employee_count,
  period
FROM hr_db.employee_profile
WHERE category_id = 1 AND is_deleted = 0
GROUP BY dept_id, dept_name, period
ORDER BY period, avg_score DESC;

-- ------------------------------------------------------------
-- 4.2 人才梯队分析视图（对应 /api/analysis/talent-pipeline）

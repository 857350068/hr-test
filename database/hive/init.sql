  AVG(value) AS avg_value,
  SUM(value) AS total_value,
  COUNT(DISTINCT employee_no) AS employee_count,
  MIN(value) AS min_value,
  MAX(value) AS max_value,
  period
FROM hr_db.employee_profile ep
WHERE ep.is_deleted = 0
GROUP BY category_id, dept_id, dept_name, period
ORDER BY period, category_id, avg_value DESC;


CREATE VIEW hr_db.vw_compensation_benefit AS
SELECT
  dept_id,
  dept_name,
  AVG(value) AS avg_salary,
  SUM(value) AS total_salary,
  COUNT(DISTINCT employee_no) AS employee_count,
  MIN(value) AS min_salary,
  MAX(value) AS max_salary,
  period
FROM hr_db.employee_profile
WHERE category_id = 3 AND is_deleted = 0
GROUP BY dept_id, dept_name, period
ORDER BY period, avg_salary DESC;


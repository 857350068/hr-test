-- ============================================================
-- 1. 创建数据库
-- ============================================================
CREATE DATABASE IF NOT EXISTS hr_db
  COMMENT '人力资源数据中心数仓库，与 MySQL hr_db 结构完全对齐'
  LOCATION '/user/hive/warehouse/hr_db.db';

USE hr_db;

-- ============================================================
-- 2. 创建表结构 (完全对齐MySQL)
-- ============================================================

-- ------------------------------------------------------------
-- 2.1 hr_department - 部门表
-- ------------------------------------------------------------
DROP TABLE IF EXISTS hr_db.hr_department;
CREATE TABLE hr_db.hr_department (
  id          BIGINT   COMMENT '主键ID',
  name        STRING   COMMENT '部门名称',
  parent_id   BIGINT   COMMENT '父部门ID: 0=顶级',
  sort_order  INT      COMMENT '排序序号'
)
COMMENT '部门表'
STORED AS PARQUET;

-- ------------------------------------------------------------
-- 2.2 sys_user - 用户表
-- ------------------------------------------------------------
DROP TABLE IF EXISTS hr_db.sys_user;
CREATE TABLE hr_db.sys_user (
  id          BIGINT   COMMENT '主键ID',
  username    STRING   COMMENT '工号',
  password    STRING   COMMENT 'BCrypt加密密码',
  name        STRING   COMMENT '姓名',
  role        STRING   COMMENT '角色: HR_ADMIN/DEPT_HEAD/MANAGEMENT/EMPLOYEE',
  dept_id     BIGINT   COMMENT '所属部门ID',
  dept_name   STRING   COMMENT '部门名称',
  dept_scope  STRING   COMMENT '部门范围JSON数组 [101,102,103]',
  phone       STRING   COMMENT '手机号',
  email       STRING   COMMENT '邮箱',
  status      TINYINT  COMMENT '状态: 0=禁用 1=启用',
  is_deleted  TINYINT  COMMENT '逻辑删除: 0=正常 1=已删除',
  create_time STRING   COMMENT '创建时间',
  update_time STRING   COMMENT '更新时间'
)
COMMENT '用户表'
STORED AS PARQUET;

-- ------------------------------------------------------------
-- 2.3 hr_data_category - 数据分类表
-- ------------------------------------------------------------
DROP TABLE IF EXISTS hr_db.hr_data_category;
CREATE TABLE hr_db.hr_data_category (
  id          BIGINT   COMMENT '主键ID',
  name        STRING   COMMENT '分类名称',
  parent_id   BIGINT   COMMENT '父分类ID: 0=顶级',
  sort_order  INT      COMMENT '排序序号'
)
COMMENT '数据分类表'
STORED AS PARQUET;

-- ------------------------------------------------------------
-- 2.4 employee_profile - 员工档案表
-- ------------------------------------------------------------
DROP TABLE IF EXISTS hr_db.employee_profile;
CREATE TABLE hr_db.employee_profile (
  id            BIGINT   COMMENT '主键ID',
  employee_no   STRING   COMMENT '员工编号',
  name          STRING   COMMENT '姓名',
  dept_id       BIGINT   COMMENT '部门ID',
  dept_name     STRING   COMMENT '部门名称(冗余)',
  job           STRING   COMMENT '岗位',
  category_id   BIGINT   COMMENT '数据分类ID (1-8)',
  value         FLOAT    COMMENT '指标值',
  period        STRING   COMMENT '统计周期(YYYYMM)',
  is_deleted    TINYINT  COMMENT '逻辑删除: 0=正常 1=已删除',
  create_time   STRING   COMMENT '创建时间'
)
COMMENT '员工档案表'
STORED AS PARQUET;

-- ------------------------------------------------------------
-- 2.5 warning_rule - 预警规则表
-- ------------------------------------------------------------
DROP TABLE IF EXISTS hr_db.warning_rule;
CREATE TABLE hr_db.warning_rule (
  id            BIGINT   COMMENT '主键ID',
  rule_type     STRING   COMMENT '规则类型: FLIGHT_RISK/TALENT_SHORTAGE/COST_OVER',
  threshold     FLOAT    COMMENT '阈值',
  description   STRING   COMMENT '规则描述',
  effective     TINYINT  COMMENT '是否生效: 0=未生效 1=生效',
  update_log    STRING   COMMENT '修改历史JSON',
  create_time   STRING   COMMENT '创建时间',
  update_time   STRING   COMMENT '更新时间'
)
COMMENT '预警规则表'
STORED AS PARQUET;

-- ------------------------------------------------------------
-- 2.6 report_template - 报表模板表
-- ------------------------------------------------------------
DROP TABLE IF EXISTS hr_db.report_template;
CREATE TABLE hr_db.report_template (
  id            BIGINT   COMMENT '主键ID',
  name          STRING   COMMENT '模板名称',
  description   STRING   COMMENT '模板描述',
  category      STRING   COMMENT '模板分类',
  content       STRING   COMMENT '模板内容(JSON格式)',
  query_sql     STRING   COMMENT '查询语句',
  parameters    STRING   COMMENT '参数配置(JSON格式)',
  chart_config  STRING   COMMENT '图表配置(JSON格式)',
  enabled       TINYINT  COMMENT '是否启用: 0=否 1=是',
  create_time   STRING   COMMENT '创建时间',
  update_time   STRING   COMMENT '更新时间',
  created_by    BIGINT   COMMENT '创建人ID',
  updated_by    BIGINT   COMMENT '更新人ID',
  version       INT      COMMENT '版本号'
)
COMMENT '报表模板表'
STORED AS PARQUET;

-- ------------------------------------------------------------
-- 2.7 sys_favorite - 收藏表
-- ------------------------------------------------------------
DROP TABLE IF EXISTS hr_db.sys_favorite;
CREATE TABLE hr_db.sys_favorite (
  id          BIGINT   COMMENT '主键ID',
  user_id     BIGINT   COMMENT '用户ID',
  fav_type    STRING   COMMENT '收藏类型: REPORT/WARNING/TALENT',
  report_id   BIGINT   COMMENT '报表ID',
  title       STRING   COMMENT '标题',
  create_time STRING   COMMENT '创建时间'
)
COMMENT '收藏表'
STORED AS PARQUET;

-- ------------------------------------------------------------
-- 2.8 data_sync_log - 数据同步日志表
-- ------------------------------------------------------------
DROP TABLE IF EXISTS hr_db.data_sync_log;
CREATE TABLE hr_db.data_sync_log (
  id                  BIGINT   COMMENT '主键ID',
  start_time          STRING   COMMENT '同步开始时间',
  end_time            STRING   COMMENT '同步结束时间',
  status              STRING   COMMENT '状态: SUCCESS/FAILED',
  records_processed   BIGINT   COMMENT '处理记录数',
  details             STRING   COMMENT '同步详情',
  create_time         STRING   COMMENT '创建时间'
)
COMMENT '数据同步日志表'
STORED AS PARQUET;

-- ------------------------------------------------------------
-- 2.9 sys_log - 操作日志表
-- ------------------------------------------------------------
DROP TABLE IF EXISTS hr_db.sys_log;
CREATE TABLE hr_db.sys_log (
  id          BIGINT   COMMENT '主键ID',
  user_id     BIGINT   COMMENT '操作人ID',
  username    STRING   COMMENT '操作人工号',
  operation   STRING   COMMENT '操作类型',
  method      STRING   COMMENT '请求方法',
  params      STRING   COMMENT '请求参数',
  ip          STRING   COMMENT 'IP地址',
  create_time STRING   COMMENT '创建时间'
)
COMMENT '操作日志表'
STORED AS PARQUET;

